#include <iostream>
#include <iomanip>
#include <stdio.h>
#include <cmath>
using namespace std;
int main() {
	double m = 16, r, w, j = 0.1, a[3] { 0.2, -4, 0.6 }, c;
	cin >> c;
	for (int n = 0; n < size(a); n++) {
		for (int f = 0; f < size(a); f++)
			w = tan(c / 3) + exp(c / m);
		r = 0.9 * sqrt(w + j) + abs(c * c + 1);
		cout << w << endl;
		cout << r << endl;
	}
	
}
